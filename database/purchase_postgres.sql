DROP TABLE IF EXISTS purchase;
CREATE TABLE purchase (
    no INTEGER PRIMARY KEY,
    invoice VARCHAR(50),
    no_faktur VARCHAR(50),
    tgl_faktur DATE,
    tipe_byr VARCHAR(20),
    salesman VARCHAR(50),
    tgl_jth_tmp VARCHAR(50),
    id_part VARCHAR(50),
    barcode VARCHAR(50),
    kode_brg VARCHAR(50),
    waktu TIMESTAMP,
    waktu_update VARCHAR(50),
    nama_barang VARCHAR(255),
    satuan_brg VARCHAR(20),
    jenis VARCHAR(20),
    suplier VARCHAR(100),
    kode_suplier VARCHAR(50),
    alamat_suplier VARCHAR(255),
    harga_beli NUMERIC,
    qty_add NUMERIC,
    subtotal NUMERIC,
    disc NUMERIC,
    nom_disc NUMERIC,
    nom_ppn NUMERIC,
    ttl_nett NUMERIC,
    qty_rcv NUMERIC,
    tgl_rcv DATE,
    "user" VARCHAR(50)
);
